export default function Portas(){
  const history = useNavigate(); 
  useEffect(() => {
    if(localStorage.getItem('token') === null){
      history('/')
    }
    if(localStorage.getItem('auth') !== "PORTAS"){
      history('/admin')
    }
  })
return (<>
<TablaPortas></TablaPortas>
</>)
}